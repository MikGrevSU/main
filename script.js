function loadQuiz() {
    const quizContainer = document.getElementById('quiz');
    quizData.forEach((quizItem, index) => {
        const questionElement = document.createElement('div');
        questionElement.classList.add('question');
        questionElement.innerHTML = `
            <p>${index + 1}. ${quizItem.question}</p>
            ${quizItem.options.map((option, i) => `
                <label>
                    <input type="radio" name="question${index}" value="${i}" required>
                    ${option}
                </label>
            `).join('')}
        `;
        quizContainer.appendChild(questionElement);
    });
}

function calculateScore(event) {
    event.preventDefault();
    let score = 0;
    quizData.forEach((quizItem, index) => {
        const selectedAnswer = document.querySelector(`input[name="question${index}"]:checked`);
        if (selectedAnswer && parseInt(selectedAnswer.value) === quizItem.answer) {
            score++;
        }
    });
    const result = document.getElementById('result');
    result.innerHTML = `Вы набрали ${score} из ${quizData.length} баллов.`;
    result.classList.remove('hidden');
}

document.getElementById('quizForm').addEventListener('submit', calculateScore);
loadQuiz();
